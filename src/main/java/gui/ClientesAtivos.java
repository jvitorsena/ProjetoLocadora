    private void carregaTabelaAtivos(){
        
        DefaultTableModel modelo = (DefaultTableModel) tbClientes.getModel();
        modelo.setNumRows(0);
        
        DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();
        
        esquerda.setHorizontalAlignment(SwingConstants.LEFT);
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        direita.setHorizontalAlignment(SwingConstants.RIGHT);
        
        
        tbClientes.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        tbClientes.getColumnModel().getColumn(1).setCellRenderer(centralizado);
        tbClientes.getColumnModel().getColumn(2).setCellRenderer(centralizado);
        tbClientes.getColumnModel().getColumn(3).setCellRenderer(centralizado);
        tbClientes.getColumnModel().getColumn(4).setCellRenderer(centralizado);
        tbClientes.getColumnModel().getColumn(5).setCellRenderer(centralizado);
        tbClientes.getColumnModel().getColumn(6).setCellRenderer(centralizado);
        
        ClienteDB clientedb = new ClienteDB();
        ArrayList<Cliente> clientes = clientedb.listarClientesAtivos();
        
        if (clientes != null){
            for (Cliente c : clientes){
                
                modelo.addRow(new Object[]{
                    
                    c.getIdCliente(),
                    c.getNome(),
                    c.getCPF(),
                    c.getEndereco(),
                    c.getTelefone(),
                    c.getEmail(),
                    c.getCNH()
                     
                    
                    
                });
                
            }
        }

    }
    
    private void limparCampos(){
        this.txtCode.setText("");
        this.txtNome.setText("");
        this.txtCPF.setText("");
        this.txtEndereco.setText("");
        this.txtTelefone.setText("");
        this.txtEmail.setText("");
        this.txtCNH.setText("");
    }
    
    private boolean verificaCampos(){
        
        if ("".equals(this.txtNome.getText()) || "".equals(this.txtCPF.getText()) || "".equals(this.txtEndereco.getText()) || "".equals(this.txtTelefone.getText()) || "".equals(this.txtEmail.getText()) || "".equals(this.txtCNH.getText())){
            return true;
        } else {
            return false;
        }
    }

