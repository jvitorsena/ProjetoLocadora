    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:

        if ("".equals(this.txtCode.getText())){

            JOptionPane.showMessageDialog(rootPane, "Por favor, selecione um registro na tabela.");

        } else if (verificaCampos() == true){

            JOptionPane.showMessageDialog(rootPane, "Por favor, preencha todos os campos.");

        } else {

            String nome = txtNome.getText();

            Cliente c = new Cliente();
            ClienteDB cdb = new ClienteDB();

            int index = tbClientes.getSelectedRow(); // retorna a linha selecionada

            c = cdb.listarClientesAtivos().get(index);

            c.setNome(txtNome.getText());
            c.setCPF(txtCPF.getText());
            c.setEndereco(txtEndereco.getText());
            c.setTelefone(txtTelefone.getText());
            c.setEmail(txtEmail.getText());
            c.setCNH(txtCNH.getText());

            cdb.alterar(c);
            carregaTabelaAtivos();
            limparCampos();
            desabilitarCampos();
            btnDesativar.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnEditar.setEnabled(false);

            JOptionPane.showMessageDialog(rootPane, "Cliente " + nome + " alterado com sucesso !");

        }
        //        cdb.inserir(c);

    }//GEN-LAST:event_btnSalvarActionPerformed

