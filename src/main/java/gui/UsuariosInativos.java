    private void carregaTabela(){
        
        DefaultTableModel modelo = (DefaultTableModel) tbUsuarios.getModel();
        modelo.setNumRows(0);
        
        DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();
        
        esquerda.setHorizontalAlignment(SwingConstants.LEFT);
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        direita.setHorizontalAlignment(SwingConstants.RIGHT);
        
        
        tbUsuarios.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        tbUsuarios.getColumnModel().getColumn(1).setCellRenderer(centralizado);
        tbUsuarios.getColumnModel().getColumn(2).setCellRenderer(centralizado);
        
        UsuarioDB usuariodb = new UsuarioDB();
        ArrayList<Usuario> usuarios = usuariodb.listarUsuariosInativos();
        
        if (usuarios != null) {
            for (Usuario u : usuarios){
                
                modelo.addRow(new Object[]{
                    
                    u.getId(),
                    u.getUsername(),
                    u.getCreatedAt(),
                    u.isIsActive()
                
                
                });
                
            }
        }
        

    }
    
