    private void btnDesativarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesativarActionPerformed
        // TODO add your handling code here:
        
        if ("".equals(this.txtIdUsuario.getText())){

            JOptionPane.showMessageDialog(rootPane, "Por favor, selecione um registro na tabela.");

        } else {

            String usuario = txtUsuario.getText();
            
            Usuario u = new Usuario();
            UsuarioDB udb = new UsuarioDB();
            
            int index = tbUsuarios.getSelectedRow();
            
            u = udb.listarUsuariosInativos().get(index);
            
            txtIdUsuario.setText(String.valueOf(u.getId()));

            udb.ativarUsuario(u);
            carregaTabela();
            limparCampos();

            JOptionPane.showMessageDialog(rootPane, "Usuario " + usuario + " ativado com sucesso !");

        }
    }//GEN-LAST:event_btnDesativarActionPerformed

