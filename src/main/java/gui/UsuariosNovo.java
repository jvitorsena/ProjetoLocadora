    private boolean comparePasswords() {
        String password = new String(this.jPasswordField1.getPassword());
        String confirmPassword = new String(this.jPasswordField2.getPassword());
        
        if (password.isEmpty() || (!password.isEmpty() && !confirmPassword.isEmpty() && !password.equals(confirmPassword))) {
            this.jLabel4.setVisible(true);
            this.jLabel4.setText("Confirmação de senha incorreta");
                    
            return false;
        }
        
        return true;
    }
