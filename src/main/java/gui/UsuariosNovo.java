    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        UsuarioDB userDB = new UsuarioDB();
        
        boolean ok = this.comparePasswords();
        if (ok) {
            Usuario user = userDB.findUserByUsername(jTextField1.getText());
            if(user.getUsername() == null) {
                user.setUsername(jTextField1.getText());
                user.setPassword(new String(this.jPasswordField1.getPassword()));
                
                ok = userDB.inserir(user);
                
                if (ok) {
                    JOptionPane.showMessageDialog(rootPane, "Cadastro efetuado com sucesso");

                    Home homeObj = new Home();
                    homeObj.setLocationRelativeTo(null);
                    homeObj.setVisible(true);

                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Falha ao efetuar cadastro");
                }
            } else {
                this.jLabel4.setVisible(true);
                this.jLabel4.setText("Usuário já cadastrado.");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

